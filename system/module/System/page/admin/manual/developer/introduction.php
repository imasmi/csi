<h1>Introduction</h1>
<span>ImaSmi Web is web development framework.</span>
<span>It can be used to create web applications such as website, business managment solution, blog, e-commerce and etc.</span>
    
    <h2>Functionality</h2>
        <span>A web application nowadays contains a lot of elements. Some of them are pretty standart and included in the most of the projects around the web. ImaSmi Web includes some of them as a pre-build functionality to speed up web development and make it easier. Here are some of the main functionalities:</span>
        <ul>
            <li>
                <h4>Languages</h4>
                <p>ImaSmi Web has its own native language managment system for input and output. It is integrated with the database and allows the addition of unlimited numbers of languages.</p>
            </li>
            <li>
                <h4>Users</h4>
                <p>User authentication system with user roles, information and addresses is available.</p>
            </li>
            <li>
                <h4>Files</h4>
                <p>This module is taking care for the file managment and processing. It resizes files for different devices screen sizes to provide best user experience. It manage html video and audio files and also is in support for creating file galleries from different types.</p>
            </li>
            <li>
                <h4>Store</h4>
                <p>Web store with a lots of free and paid themes and plugins is available. With an easy to install graphical user interface, the desired final product is just a few clicks away.</p>
            </li>
        </ul>
    
    <h2>Technology</h2>
        <span>As the web development requires more than one technology, ImaSmi Web is not a concrete programming language platform, but instead, it combines several different web technologies and programming languages to have versatile functionality. Let's look at them one by one: </span>
        <ul>
            <li>
                <h4>HTML</h4>
                <p>Hypertext Markup Language (HTML) is the way web applications are displayed. It is the main technology for rendrering web pages and without it a web application can hardly exists.</p>
            </li>
            <li>
                <h4>CSS</h4>
                <p>Cascading Style Sheets (CSS) is language used for style control of HTML documents. It can customize presentation of fonts, colors, display options and positions and many more.</p>
            </li>
            <li>
                <h4>JavaScript</h4>
                <p>JavaScript or just JS is a high-level just-in-time compiled language. HTML, CSS and JavaScript are the core tehnologies of the World Wide Web and how the browsers works. It is created for client side browser behavior and this is exactly what ImaSmi Web uses it for.</p>
            </li>
            <li>
                <h4>PHP</h4>
                <p>PHP: Hypertext Preprocessor or just PHP is a server side interpreted language. It is designed and suited for web deveopment and can be mixed with HTML to retreive server and database information as HTML output. ImaSmi Web is server based application and this is why PHP is choosen for structure building and back-end communication in the system.</p>
            </li>
            <li>
                <h4>Database</h4>
                <p>Databases are varios data entries stored in computer systems. ImaSmi Web application can't operate without database. It uses PHP PDO extension to communicate with the database service and to insert and read data from it. The preferred database service to use is MySQL, but PDO extension supports other options as well. You can read more about supported databases <a href="https://www.php.net/manual/en/pdo.drivers.php" target="blank">here</a>.</p>
            </li>
        </ul>