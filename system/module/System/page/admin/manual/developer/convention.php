<h1>Naming conventions</h1>
    <span>This naming convections are optional and they are not a must at any price, but ImaSmi Web follows them as much as possible and they are stongly recommended. They are created for better readability and recognizability of the different technologies implemented in the system.</span>
<ul>
    <li>
        <h4>URL</h4>
        <p>Use hippens to separate words</p>
        <p>Example: hippen-sepparated</p>
    </li>
    <li>
        <h4>Filenames</h4>
        <p>Use lowercase only to prevent problems with case-insensitive systems.</p>
        <p>Use hippens to separate words to follow the rule for url and make filenames SEO friendly for search engines.</p>
        <p>Example: hippen-sepparated.php</p>
    </li>
    <li>
        <h4>CSS</h4>
        <p>Use hippens to separate words in classes and ids</p>
        <p>Example: hippen-sepparated</p>
    </li>
    <li>
        <h4>JavaScript</h4>
        <p>Use camelCase to separate words</p>
        <p>Example: var camelCase; function camelCase(){}</p>
    </li>
    <li>
        <h4>PHP</h4>
        <p>Objects: use StudlyCaps for objects and variables containing objects</p>
        <p>Example: $MyClass = new MyClass();
        <p>Functions and variables: use underscores to separate words</p>
        <p>Example: $my_variable, function my_function(){}</p>
    </li>
    <li>
        <h4>MySQL</h4>
        <p>Use underscores to separate words in table and column names</p>
        <p>Example: table_name</p>
    </li>
    <li>
        <h4>HTML Forms</h4>
        <p>Use underscores to separate words in form input names</p>
        <p>Example: form_input_name</p>
    </li>
</ul>